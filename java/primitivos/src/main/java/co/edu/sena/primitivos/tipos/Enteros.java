package co.edu.sena.primitivos.tipos;

public class Enteros {
	
	public static void main(String[] args) {
		// BYTE
		// tipo de dato byte
		// caracteristica de los bytes
		System.out.println("el tipo de dato es: " + Byte.TYPE);
		System.out.println("tamaño en byte: " + Byte.SIZE + "bits");
		System.out.println("el valor maximo es: " + Byte.MAX_VALUE);
		System.out.println("el valor minimo es: " + Byte.MIN_VALUE);
		System.out.println("-------------------------------------------");

		// short
		// entero corto
		System.out.println("nombre del tipo de dato: " + Short.TYPE);
		System.out.println("tamaño en bits: " + Short.BYTES + " bytes");
		System.out.println("tamaño maximo: " + Short.MAX_VALUE);
		System.out.println("tamaño minimo: " + Short.MIN_VALUE);
		System.out.println("-------------------------------------------");
		// int
		// entero
		System.out.println("nombre del tipo de dato: " + Integer.TYPE);
		System.out.println("tamaño en bits: " + Integer.BYTES + " bytes");
		System.out.println("tamaño maximo: " + Integer.MAX_VALUE);
		System.out.println("tamaño minimo: " + Integer.MIN_VALUE);
		System.out.println("-------------------------------------------");
		// long
		// entero largo
		System.out.println("nombre del tipo de dato: " + Long.TYPE);
		System.out.println("tamaño en bits: " + Long.BYTES + " bytes");
		System.out.println("tamaño maximo: " + Long.MAX_VALUE);
		System.out.println("tamaño minimo: " + Long.MIN_VALUE);
		System.out.println("-------------------------------------------");
		// char
		// caracter
		System.out.println("nombre del tipo de dato: " + Character.TYPE);
		System.out.println("tamaño en bits: " + Character.BYTES + " bytes");

		
		

	}

}
