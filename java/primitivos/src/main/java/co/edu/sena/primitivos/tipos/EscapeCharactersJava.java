package co.edu.sena.primitivos.tipos;

public class EscapeCharactersJava {

	public static void main(String[] args) {
		System.out.println("222\b");
		System.out.println("222\t4455");
		System.out.println("222\n4455");
		
		System.out.println("2224455\r8");
		System.out.println("\"hola\"");
		System.out.println("c:\\user\\archivo.txt");
	}

}
